package 实验二.content;

/**
 * {@code Shape} is used to describe the
 * 2D properties of a drawing.
 */
public interface Shape{
    /**
     * field area's getter
     * @return the area of shape.
     */
    double getArea();

    /**
     * field perimeter's getter
     * @return the perimeter of shape.
     */
    double getPerimeter();
}





